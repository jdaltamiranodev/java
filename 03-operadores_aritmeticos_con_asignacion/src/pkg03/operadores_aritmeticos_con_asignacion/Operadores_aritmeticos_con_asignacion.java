
package pkg03.operadores_aritmeticos_con_asignacion;

public class Operadores_aritmeticos_con_asignacion {

    public static void main(String[] args) {
        int numero = 10;
        int numero1 = 10;
        
        numero += 5; // numero = numero + 5;
        numero -= 5; // numero = numero - 5;
        
        System.out.println("" + numero);
        System.out.println("" + numero1);
    }
    
}
